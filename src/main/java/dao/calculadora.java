
package dao;


public class calculadora {


    public calculadora() {
        
    }
    public double sumar(double num01, double num02){
    
        return num01+num02;
    }
    
    public double restar(double num01, double num02){
    
        return num01-num02;
    }
    
    public double multiplicar(double num01, double num02){
    
        return num01*num02;
    }
    
    public double dividir(double num01, double num02){
    
        return num01/num02;
    }

     
}
